package a3.model;

public class Animal {
	
	private String name;
	private boolean hair;
	private boolean feathers;
	private boolean eggs;
	private boolean milk;
	private boolean airborne;
	private boolean aquatic;
	private boolean predator;
	private boolean toothed;
	private boolean backbone;
	private boolean breathes;
	private boolean venomous;
	private boolean fins;
	private int legs;
	private boolean tail;
	private boolean domestic;
	private boolean catsize;
	private int type;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public boolean isHair() {
		return hair;
	}
	
	public void setHair(boolean hair) {
		this.hair = hair;
	}
	
	public boolean isFeathers() {
		return feathers;
	}
	
	public void setFeathers(boolean feathers) {
		this.feathers = feathers;
	}
	
	public boolean isEggs() {
		return eggs;
	}
	
	public void setEggs(boolean eggs) {
		this.eggs = eggs;
	}
	
	public boolean isMilk() {
		return milk;
	}
	
	public void setMilk(boolean milk) {
		this.milk = milk;
	}
	
	public boolean isAirborne() {
		return airborne;
	}
	
	public void setAirborne(boolean airborne) {
		this.airborne = airborne;
	}
	
	public boolean isAquatic() {
		return aquatic;
	}
	
	public void setAquatic(boolean aquatic) {
		this.aquatic = aquatic;
	}
	
	public boolean isPredator() {
		return predator;
	}
	
	public void setPredator(boolean predator) {
		this.predator = predator;
	}
	
	public boolean isToothed() {
		return toothed;
	}
	
	public void setToothed(boolean toothed) {
		this.toothed = toothed;
	}
	
	public boolean isBackbone() {
		return backbone;
	}
	
	public void setBackbone(boolean backbone) {
		this.backbone = backbone;
	}
	
	public boolean isBreathes() {
		return breathes;
	}
	
	public void setBreathes(boolean breathes) {
		this.breathes = breathes;
	}
	
	public boolean isVenomous() {
		return venomous;
	}
	
	public void setVenomous(boolean venomous) {
		this.venomous = venomous;
	}
	
	public boolean isFins() {
		return fins;
	}
	
	public void setFins(boolean fins) {
		this.fins = fins;
	}
	
	public int getLegs() {
		return legs;
	}
	
	public void setLegs(int legs) {
		this.legs = legs;
	}
	
	public boolean isTail() {
		return tail;
	}
	
	public void setTail(boolean tail) {
		this.tail = tail;
	}
	
	public boolean isDomestic() {
		return domestic;
	}
	
	public void setDomestic(boolean domestic) {
		this.domestic = domestic;
	}
	
	public boolean isCatsize() {
		return catsize;
	}
	
	public void setCatsize(boolean catsize) {
		this.catsize = catsize;
	}
	
	public int getType() {
		return type;
	}
	
	public void setType(int type) {
		this.type = type;
	}
	
}
